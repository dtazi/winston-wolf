"""Claude Code CLI subprocess implementation of ICPExtractor.

Why subprocess instead of the Anthropic Python SDK:
  The user runs Winston Wolf on a Max subscription. The Anthropic API is
  billed separately from claude.ai, and using Max-OAuth tokens with the
  Python SDK is not permitted. Claude Code's CLI, however, accepts a
  long-lived OAuth token (CLAUDE_CODE_OAUTH_TOKEN, generated by
  `claude setup-token`) and bills its inference against Max. So for our
  evaluation-scale workloads we shell out to `claude -p`.

# TODO(pre-production): Once Scout volumes exceed Max rate limits, swap
# this for a parallel ClaudeAPIExtractor using the `anthropic` SDK.
# Trigger conditions and migration path documented in the project memory
# file ``project_migration_todos.md``.
"""

from __future__ import annotations

import os
import subprocess

DEFAULT_MODEL = "haiku"  # Claude Code's alias for the current Haiku model
MAX_INPUT_CHARS = 12_000
SUBPROCESS_TIMEOUT_SECONDS = 120

ICP_EXTRACTION_PROMPT = """You are an analyst extracting a company's ICP \
(ideal-customer profile) for the purpose of finding similar competitors via web search.

The customer wants competitors for ONE SPECIFIC BUSINESS LINE of this company. \
Diversified conglomerates have multiple unrelated business lines — IGNORE the others.

Target business line / focus: **{focus}**

Read the content below and return a single-paragraph **brand-agnostic** description \
of THIS COMPANY'S {focus} operations specifically. Emphasise:

- The focus industry / sector (NOT the company's other business lines)
- Specific product categories within the focus
- Market and geography of the focus business
- Approximate scale of the focus operation (employees, revenue, plants — if mentioned)
- Notable business characteristics relevant to competitors in this category

Strict rules:
- Do NOT mention the company's own name, brand, or any trademarked product line.
- Do NOT use any proper noun that identifies this specific company.
- Do NOT describe their other business lines outside the focus.
- The description must be usable as a search query to find OTHER companies competing in {focus}.
- **Maximum 350 characters total** — search backends reject longer queries. Be concise.
- Return ONLY the description, one paragraph, no preamble, no formatting, no quote marks.

Content from the company's page:
---
{content}
---

Brand-agnostic ICP description for {focus} (≤350 chars):"""


GENERIC_ICP_EXTRACTION_PROMPT = """You are an analyst extracting a company's ICP \
(ideal-customer profile) for the purpose of finding similar companies via web search.

Read the following content from a company's About / corporate description page, and \
return a single-paragraph **brand-agnostic** description of the company that emphasises:

- Industry / sector
- Product categories
- Market and geography
- Approximate scale (employees, revenue, plants — if mentioned)
- Notable business characteristics (integrated production, vertical model, etc.)

Strict rules:
- Do NOT mention the company's own name, brand, or any trademarked product line.
- Do NOT use any proper noun that identifies this specific company.
- The description must be usable as a search query to find OTHER companies of the same kind.
- **Maximum 350 characters total** — search backends reject longer queries. Be concise.
- Return ONLY the description, one paragraph, no preamble, no formatting, no quote marks.

Content from the company's page:
---
{content}
---

Brand-agnostic ICP description (≤350 chars):"""


class ClaudeICPExtractor:
    name = "claude-code-cli"

    def __init__(self, model: str = DEFAULT_MODEL) -> None:
        # We don't hard-error if the OAuth token is missing — the user may have
        # an interactively-authenticated claude install. We surface the
        # underlying error at call time if the CLI refuses the request.
        self.model = model

    def extract_icp(
        self,
        content: str,
        *,
        source_url: str | None = None,
        focus: str | None = None,
    ) -> str:
        truncated = content[:MAX_INPUT_CHARS]
        if focus:
            prompt = ICP_EXTRACTION_PROMPT.format(content=truncated, focus=focus)
        else:
            prompt = GENERIC_ICP_EXTRACTION_PROMPT.format(content=truncated)
        result = subprocess.run(
            [
                "claude",
                "-p",
                "--model",
                self.model,
                "--output-format",
                "text",
                "--no-session-persistence",
            ],
            input=prompt,
            capture_output=True,
            text=True,
            timeout=SUBPROCESS_TIMEOUT_SECONDS,
            check=False,
            env=_subprocess_env(),
        )
        if result.returncode != 0:
            raise RuntimeError(
                f"claude CLI exited {result.returncode}: {result.stderr.strip() or result.stdout.strip()}"
            )
        return result.stdout.strip()


def _subprocess_env() -> dict[str, str]:
    """Inherit the parent environment so CLAUDE_CODE_OAUTH_TOKEN flows through.

    Returning a fresh dict (instead of relying on the default of inheriting
    silently) makes the dependency on the token explicit and lets us extend
    it later (e.g. forcing a working directory, suppressing auto-update).
    """
    return dict(os.environ)
